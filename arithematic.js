const num1 = 12;
const num2 = 2;

// add, subtract, divide, multiply, remainder, power

// + operator for addition
num1 + num2;
const addition = num1 + num2;

// - operator for subtraction
const subtraction = num1 - num2;

// * for multiplication
const product = num1 * num2;

// / operator for division
const division = num1 / num2;
// results in a floating number
// except when the division is complete.


// % (mod) for remainder
const remainder = num1 % num2;

// ** for power
const power = num1 ** num2; // 12 ^ 2

console.log(addition, subtraction, division, product, remainder, power);
