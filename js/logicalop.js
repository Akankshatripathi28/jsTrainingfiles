// and, or
// comparison of 2 booleans
// return 1 boolean

// or --> ||
// or tends to true
// returns false when both booleans are false
true  ||  true   // true
true  ||  false  // true
false ||  true   // true
false ||  false  // false


// and --> &&
// and tends to false
// returns true when both booleans are true
true  &&  true   // true
true  &&  false  // false
false &&  true   // false
false &&  false  // false

